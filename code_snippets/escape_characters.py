# single quote
txt = 'It\'s alright.'
print(txt)

# backslash
txt = "This will insert one \\ (backslash)."
print(txt)

# new line
txt = "Hello\nWorld!"
print(txt)

# carriage return (enter key)
txt = "Hello\rWorld!"
print(txt)

# tab
txt = "Hello\tWorld!"
print(txt)

#This example erases one character (backspace):
txt = "Hello \bWorld!"
print(txt)
